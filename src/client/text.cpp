void TextRenderer::generateFontTexture() {
	// Generate a Signed Distance Field texture from the 8x8 bitmap font.
	// This gives smooth, crisp text at any scale with anti-aliased edges.
	const int SCALE = 4;            // upscale: each 8x8 glyph becomes 32x32
	const int GLYPH = 8 * SCALE;    // 32 px per glyph
	const int GRID_W = 16, GRID_H = 6;
	const int TEX_W = GRID_W * GLYPH;  // 512
	const int TEX_H = GRID_H * GLYPH;  // 192
	const int SEARCH = 6;           // search radius in upscaled pixels
	const float SPREAD = (float)SEARCH;

	// Step 1: upscale 8x8 bitmap to 32x32 per glyph
	std::vector<bool> bitmap(TEX_W * TEX_H, false);
	for (int ch = 0; ch < 96; ch++) {
		int gx = (ch % GRID_W) * GLYPH;
		int gy = (ch / GRID_W) * GLYPH;
		for (int row = 0; row < 8; row++) {
			unsigned char bits = FONT_8X8[ch][row];
			for (int col = 0; col < 8; col++) {
				if (bits & (1 << col)) {
					for (int sy = 0; sy < SCALE; sy++)
						for (int sx = 0; sx < SCALE; sx++)
							bitmap[(gy + row*SCALE + sy) * TEX_W + (gx + col*SCALE + sx)] = true;
				}
			}
		}
	}

	// Step 2: compute SDF -- for each pixel, find distance to nearest opposite pixel
	std::vector<unsigned char> sdf(TEX_W * TEX_H);
	for (int y = 0; y < TEX_H; y++) {
		for (int x = 0; x < TEX_W; x++) {
			bool inside = bitmap[y * TEX_W + x];
			float minDistSq = SPREAD * SPREAD;

			for (int dy = -SEARCH; dy <= SEARCH; dy++) {
				int ny = y + dy;
				if (ny < 0 || ny >= TEX_H) continue;
				for (int dx = -SEARCH; dx <= SEARCH; dx++) {
					int nx = x + dx;
					if (nx < 0 || nx >= TEX_W) continue;
					if (bitmap[ny * TEX_W + nx] != inside) {
						float dsq = (float)(dx*dx + dy*dy);
						if (dsq < minDistSq) minDistSq = dsq;
					}
				}
			}

			float dist = std::sqrt(minDistSq);
			// Normalize: inside > 0.5, outside < 0.5
			float val = inside ? (0.5f + 0.5f * dist / SPREAD)
			                   : (0.5f - 0.5f * dist / SPREAD);
			sdf[y * TEX_W + x] = (unsigned char)(std::clamp(val, 0.0f, 1.0f) * 255.0f);
		}
	}

	// Step 3: upload with LINEAR filtering for smooth interpolation
	glGenTextures(1, &m_fontTexture);
	glBindTexture(GL_TEXTURE_2D, m_fontTexture);
	glTexImage2D(GL_TEXTURE_2D, 0, GL_RED, TEX_W, TEX_H, 0,
		GL_RED, GL_UNSIGNED_BYTE, sdf.data());
	glTexParameteri(GL_TEXTURE_2D, GL_TEXTURE_MIN_FILTER, GL_LINEAR);
	glTexParameteri(GL_TEXTURE_2D, GL_TEXTURE_MAG_FILTER, GL_LINEAR);
	glTexParameteri(GL_TEXTURE_2D, GL_TEXTURE_WRAP_S, GL_CLAMP_TO_EDGE);
	glTexParameteri(GL_TEXTURE_2D, GL_TEXTURE_WRAP_T, GL_CLAMP_TO_EDGE);
}

bool TextRenderer::init(const std::string& shaderDir) {
	if (!m_textShader.loadFromFile(shaderDir + "/text.vert", shaderDir + "/text.frag"))
		return false;

	generateFontTexture();

	// Dynamic VBO for text quads
	glGenVertexArrays(1, &m_vao);
	glGenBuffers(1, &m_vbo);
	glBindVertexArray(m_vao);
	glBindBuffer(GL_ARRAY_BUFFER, m_vbo);
	glBufferData(GL_ARRAY_BUFFER, 4096 * sizeof(float), nullptr, GL_DYNAMIC_DRAW);
	// pos (2) + uv (2) = 4 floats per vertex
	glVertexAttribPointer(0, 2, GL_FLOAT, GL_FALSE, 4 * sizeof(float), (void*)0);
	glEnableVertexAttribArray(0);
	glVertexAttribPointer(1, 2, GL_FLOAT, GL_FALSE, 4 * sizeof(float), (void*)(2 * sizeof(float)));
	glEnableVertexAttribArray(1);
	glBindVertexArray(0);
	return true;
}

void TextRenderer::shutdown() {
	if (m_fontTexture) glDeleteTextures(1, &m_fontTexture);
	if (m_vbo) glDeleteBuffers(1, &m_vbo);
	if (m_vao) glDeleteVertexArrays(1, &m_vao);
}

static void buildTextVerts(std::vector<float>& verts, const std::string& text,
                           float x, float y, float scale) {
	float charW = scale * 0.018f;
	float charH = scale * 0.032f;

	float cx = x;
	for (char c : text) {
		int idx = c - 32;
		if (idx < 0 || idx >= 96) idx = 0;
		float u0 = (idx % 16) / 16.0f;
		float v0 = (idx / 16) / 6.0f;
		float u1 = u0 + 1.0f / 16.0f;
		float v1 = v0 + 1.0f / 6.0f;

		float vd[] = {
			cx,        y,        u0, v1,
			cx+charW,  y,        u1, v1,
			cx+charW,  y+charH,  u1, v0,
			cx,        y,        u0, v1,
			cx+charW,  y+charH,  u1, v0,
			cx,        y+charH,  u0, v0,
		};
		verts.insert(verts.end(), std::begin(vd), std::end(vd));
		cx += charW;
	}
}

void TextRenderer::drawText(const std::string& text, float x, float y,
                            float scale, glm::vec4 color, float aspect) {
	std::vector<float> verts;
	buildTextVerts(verts, text, x, y, scale);
	if (verts.empty()) return;

	glDisable(GL_DEPTH_TEST);
	glEnable(GL_BLEND);
	glBlendFunc(GL_SRC_ALPHA, GL_ONE_MINUS_SRC_ALPHA);

	m_textShader.use();
	m_textShader.setVec3("uColor", glm::vec3(color));
	m_textShader.setFloat("uAlpha", color.a);
	m_textShader.setInt("uFontTex", 0);
	m_textShader.setInt("uMode", 0); // SDF text

	glActiveTexture(GL_TEXTURE0);
	glBindTexture(GL_TEXTURE_2D, m_fontTexture);

	glBindVertexArray(m_vao);
	glBindBuffer(GL_ARRAY_BUFFER, m_vbo);
	glBufferSubData(GL_ARRAY_BUFFER, 0, verts.size() * sizeof(float), verts.data());
	glDrawArrays(GL_TRIANGLES, 0, (int)(verts.size() / 4));

	glDisable(GL_BLEND);
	glEnable(GL_DEPTH_TEST);
}

void TextRenderer::drawTitle(const std::string& text, float x, float y,
                             float scale, glm::vec4 color, float aspect) {
	std::vector<float> verts;
	buildTextVerts(verts, text, x, y, scale);
	if (verts.empty()) return;

	glDisable(GL_DEPTH_TEST);
	glEnable(GL_BLEND);
	glBlendFunc(GL_SRC_ALPHA, GL_ONE_MINUS_SRC_ALPHA);

	m_textShader.use();
	m_textShader.setVec3("uColor", glm::vec3(color));
	m_textShader.setFloat("uAlpha", color.a);
	m_textShader.setInt("uFontTex", 0);
	m_textShader.setInt("uMode", 2); // title mode: outline + glow

	glActiveTexture(GL_TEXTURE0);
	glBindTexture(GL_TEXTURE_2D, m_fontTexture);

	glBindVertexArray(m_vao);
	glBindBuffer(GL_ARRAY_BUFFER, m_vbo);
	glBufferSubData(GL_ARRAY_BUFFER, 0, verts.size() * sizeof(float), verts.data());
	glDrawArrays(GL_TRIANGLES, 0, (int)(verts.size() / 4));

	glDisable(GL_BLEND);
	glEnable(GL_DEPTH_TEST);
}

void TextRenderer::drawRect(float x, float y, float w, float h, glm::vec4 color) {
	float verts[] = {
		x,   y,   0, 0,
		x+w, y,   1, 0,
		x+w, y+h, 1, 1,
		x,   y,   0, 0,
		x+w, y+h, 1, 1,
		x,   y+h, 0, 1,
	};

	glDisable(GL_DEPTH_TEST);
	glEnable(GL_BLEND);
	glBlendFunc(GL_SRC_ALPHA, GL_ONE_MINUS_SRC_ALPHA);

	m_textShader.use();
	m_textShader.setVec3("uColor", glm::vec3(color));
	m_textShader.setFloat("uAlpha", color.a);
	m_textShader.setInt("uFontTex", 0);
	m_textShader.setInt("uMode", 1); // solid rect mode
	glActiveTexture(GL_TEXTURE0);
	glBindTexture(GL_TEXTURE_2D, m_fontTexture);

	glBindVertexArray(m_vao);
	glBindBuffer(GL_ARRAY_BUFFER, m_vbo);
	glBufferSubData(GL_ARRAY_BUFFER, 0, sizeof(verts), verts);
	glDrawArrays(GL_TRIANGLES, 0, 6);

	glDisable(GL_BLEND);
	glEnable(GL_DEPTH_TEST);
}

