# ModCraft Item Visual Definitions (Client-Side)
#
# These define HOW items look when equipped on characters.
# The server knows items exist and their game properties.
# The client knows how they render: geometry, colors, particle effects.
#
# Players can create custom item visuals by writing Python code
# following these base classes. When pybind11 is integrated,
# these load at runtime on the CLIENT only.
